//tüm blog yazılarını getir (en yeni en üstte)
export function tumBloglariGetir(){
    return blogYazilari.sort((a,b)=>new Date(b.tarih) - new Date(a.tarih))  //pozitifse b negaifse a en önde sralıonr
}

//slug a göre te bir blog yazısını getir

export function blogGetir(slug){
    return blogYazilari.find(blog=>blog.slug===slug)
}

//katagoriye göre blogları getir

export function kategoriyeGoreBloglariGetir(kategori){
    return blogYazilari.filter(blog=>blog.kategori===kategori)
}

//tüm katagorileri getir tekrar etmeden 

export function kategorileriGetir(){
    const kategoriler=[...new Set(blogYazilari.map(b=>b.kategori))]
    //set tekrarlayan elemanları çıakrır daha sonra bu tekrarsız katagorileri arraya çevirdik
    return kategoriler.sort() //alfabetik sıraya göre sırala
}